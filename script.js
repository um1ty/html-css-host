        function calculateBottleneck() {
            if (!selectedCpu || !selectedGpu || !selectedResolution) return;

            // Show loading indicator
            const calculateBtn = document.getElementById('calculateBtn');
            const originalText = calculateBtn.innerHTML;
            calculateBtn.innerHTML = '<span class="btn-icon">⚡</span><span class="btn-text">Analyzing...</span>';
            calculateBtn.disabled = true;

            // Use requestAnimationFrame for smooth transitions
            requestAnimationFrame(() => {
                // Show results section with smooth animation
                const resultsSection = document.getElementById('resultsSection');
                resultsSection.style.display = 'block';
                resultsSection.style.opacity = '0';
                resultsSection.style.transform = 'translateY(20px)';
                
                // Smooth fade-in animation
                setTimeout(() => {
                    resultsSection.style.transition = 'all 0.6s cubic-bezier(0.4, 0, 0.2, 1)';
                    resultsSection.style.opacity = '1';
                    resultsSection.style.transform = 'translateY(0)';
                }, 50);
                
                // Smooth scroll to results
                setTimeout(() => {
                    resultsSection.scrollIntoView({ 
                        behavior: 'smooth',
                        block: 'start',
                        inline: 'nearest'
                    });
                }, 300);
            
            // Get additional hardware selections
            const ramSize = parseInt(document.getElementById('ramSelect').value);
            const storageType = document.getElementById('storageSelect').value;
            const purpose = document.getElementById('purposeSelect').value;
            
            // Calculate bottleneck percentage
            const cpuScore = selectedCpu.score;
            const gpuScore = selectedGpu.score;
            const resolutionFactor = selectedResolution.factor;
            
            // Adjust scores based on purpose and hardware
            let adjustedCpuScore = cpuScore;
            let adjustedGpuScore = gpuScore / resolutionFactor;
            
            // Purpose-based adjustments
            switch(purpose) {
                case 'streaming':
                    adjustedCpuScore *= 1.2; // CPU more important for streaming
                    break;
                case 'editing':
                    adjustedCpuScore *= 1.3; // CPU critical for video editing
                    break;
                case 'productivity':
                    adjustedCpuScore *= 1.1;
                    break;
            }
            
            // RAM impact
            if (ramSize < 16) adjustedCpuScore *= 0.9;
            if (ramSize >= 32) adjustedCpuScore *= 1.05;
            
            // Storage impact
            if (storageType === 'nvme') {
                adjustedCpuScore *= 1.02;
                adjustedGpuScore *= 1.01;
            } else if (storageType === 'hdd') {
                adjustedCpuScore *= 0.95;
            }
            
            // Calculate bottleneck
            const difference = Math.abs(adjustedCpuScore - adjustedGpuScore);
            const maxScore = Math.max(adjustedCpuScore, adjustedGpuScore);
            const bottleneckPercentage = Math.round((difference / maxScore) * 100);
            
            // Determine limiting component
            let limitingComponent, bottleneckType, suggestion;
            if (adjustedCpuScore < adjustedGpuScore) {
                limitingComponent = 'CPU';
                bottleneckType = 'cpuBottleneck';
                suggestion = 'Consider upgrading your CPU';
            } else if (adjustedGpuScore < adjustedCpuScore) {
                limitingComponent = 'GPU';
                bottleneckType = 'gpuBottleneck'; 
                suggestion = 'Consider upgrading your GPU';
            } else {
                limitingComponent = 'Balanced';
                bottleneckType = 'balanced';
                suggestion = 'Your system is well balanced';
            }

            // Update analysis cards
            updateAnalysisCards(bottleneckPercentage, limitingComponent, suggestion);
            
            // Calculate and update FPS estimates
            updateFPSEstimates(adjustedCpuScore, adjustedGpuScore, resolutionFactor, purpose);
            
            // Update performance distribution
            updatePerformanceDistribution(adjustedCpuScore, adjustedGpuScore);
            
            // Create gaming assessment chart
            createGamingAssessmentChart(adjustedCpuScore, adjustedGpuScore, bottleneckPercentage);
            
            // Generate gaming recommendations
            generateGamingRecommendations(bottleneckPercentage, limitingComponent, purpose);
            
            // Add two more advanced features
            generateSystemOptimizations(adjustedCpuScore, adjustedGpuScore, purpose);
            generateUpgradeRoadmap(bottleneckPercentage, limitingComponent, selectedCpu, selectedGpu);
            
            // Restore button state
            setTimeout(() => {
                calculateBtn.innerHTML = originalText;
                calculateBtn.disabled = false;
                calculateBtn.classList.add('enabled');
            }, 1500);
            });
        }

