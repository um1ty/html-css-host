        async function loadHardwareData() {
            const githubUrl = 'https://raw.githubusercontent.com/um1ty/bottleneck-hardware-data/main/hardware-data.json';
            
            try {
                const response = await fetch(githubUrl);
                if (!response.ok) {
                    throw new Error(`HTTP error! status: ${response.status}`);
                }
                hardwareData = await response.json();
                console.log('Hardware data loaded from GitHub successfully');
            } catch (error) {
                console.error('GitHub fetch failed:', error);
                throw error;
            }
        }

