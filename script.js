        function updateFPSEstimates(cpuScore, gpuScore, resolutionFactor, purpose) {
            // Enhanced FPS calculation
            const baseScore = Math.min(cpuScore, gpuScore);
            const avgFps = Math.round(baseScore / (resolutionFactor * 85));
            
            // Power consumption estimate (watts)
            const cpuPower = Math.round((cpuScore / 1000) * 65);
            const gpuPower = Math.round((gpuScore / 1000) * 220);
            const totalPower = cpuPower + gpuPower + 100; // +100W for other components
            
            // Streaming performance assessment
            let streamingPerf;
            if (purpose === 'streaming') {
                if (avgFps >= 120) streamingPerf = 'Excellent - 60fps stream';
                else if (avgFps >= 90) streamingPerf = 'Good - 60fps stream';
                else if (avgFps >= 60) streamingPerf = 'Fair - 30fps stream';
                else streamingPerf = 'Poor - streaming difficult';
            } else {
                if (avgFps >= 120) streamingPerf = 'Stream Ready - 60fps';
                else if (avgFps >= 90) streamingPerf = 'Good - 60fps';
                else if (avgFps >= 60) streamingPerf = 'Fair - 30fps';
                else streamingPerf = 'Not recommended';
            }
            
            // Update FPS cards
            document.getElementById('avgFps').textContent = `${avgFps} FPS`;
            document.getElementById('powerConsumption').textContent = `${totalPower}W`;
            document.getElementById('streamingPerf').textContent = streamingPerf;
            
            // Update quality indicators
            const fpsList = [
                { element: document.querySelector('.fps-card .fps-quality'), fps: avgFps }
            ];
            
            fpsList.forEach(item => {
                if (item.fps >= 90) {
                    item.element.textContent = 'Excellent - Ultra Smooth';
                    item.element.className = 'fps-quality good';
                } else if (item.fps >= 60) {
                    item.element.textContent = 'Good - Smooth';
                    item.element.className = 'fps-quality good';
                } else if (item.fps >= 30) {
                    item.element.textContent = 'Fair - Playable';
                    item.element.className = 'fps-quality medium';
                } else {
                    item.element.textContent = 'Poor - Choppy';
                    item.element.className = 'fps-quality poor';
                }
            });
        }

