        function optimizeUpscaling() {
            if (!currentPerformance.game) {
                alert('Please run the main assessment first!');
                return;
            }
            
            const upscalingMode = document.getElementById('upscalingMode').value;
            const gameData = gamePerformanceData[currentPerformance.game];
            const upscalingSettings = upscalingData[upscalingMode];
            
            // Check DLSS support for DLSS modes
            if (upscalingMode.includes('dlss') && !gameData.dlssSupport) {
                alert('This game does not support DLSS! Try FSR instead.');
                return;
            }
            
            const baseFps = currentPerformance.fps;
            const boostedFps = Math.round(baseFps * upscalingSettings.multiplier);
            const fpsBoost = Math.round(((boostedFps - baseFps) / baseFps) * 100);
            
            let tensorUsage = 0;
            if (upscalingMode.includes('dlss')) {
                switch(upscalingMode) {
                    case 'dlss_quality': tensorUsage = 30; break;
                    case 'dlss_balanced': tensorUsage = 50; break;
                    case 'dlss_performance': tensorUsage = 70; break;
                    case 'dlss_ultra_performance': tensorUsage = 90; break;
                }
            }
            
            // Update UI
            document.getElementById('upscalingBoost').textContent = fpsBoost > 0 ? `+${fpsBoost}%` : '0%';
            document.getElementById('renderScale').textContent = upscalingSettings.renderScale + '%';
            document.getElementById('aiLatency').textContent = upscalingSettings.latency + 'ms';
            document.getElementById('imageQuality').textContent = upscalingSettings.quality + '%';
            document.getElementById('tensorUsage').textContent = tensorUsage + '%';
            document.getElementById('tensorBar').style.width = tensorUsage + '%';
            
            currentPerformance.upscaling = upscalingMode;
        }


