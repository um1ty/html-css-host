        function calculateFPS(cpuScore, gpuScore, resolutionFactor) {
            // FPS calculation: min(CPU_score, GPU_score) / resolution_factor
            const baseScore = Math.min(cpuScore, gpuScore);
            const avgFps = Math.round(baseScore / (resolutionFactor * 100));
            const oneLows = Math.round(avgFps * 0.8);
            const pointOneLows = Math.round(avgFps * 0.65);

            // Color coding based on FPS
            function getFpsColor(fps) {
                if (fps >= 60) return 'fps-good';
                if (fps >= 30) return 'fps-medium';
                return 'fps-poor';
            }

            // Update FPS display
            const avgFpsElement = document.getElementById('avgFps');
            const oneLowsElement = document.getElementById('oneLows');
            const pointOneLowsElement = document.getElementById('pointOneLows');

            avgFpsElement.textContent = avgFps;
            avgFpsElement.className = `fps-value ${getFpsColor(avgFps)}`;
            
            oneLowsElement.textContent = oneLows;
            oneLowsElement.className = `fps-value ${getFpsColor(oneLows)}`;
            
            pointOneLowsElement.textContent = pointOneLows;
            pointOneLowsElement.className = `fps-value ${getFpsColor(pointOneLows)}`;
        }

